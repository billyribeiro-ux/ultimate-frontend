				{#each messages as msg (msg.key)}
					<li class="message-item">
						<code class="message-item__key">{msg.key}</code>
						<span class="message-item__pattern">{localeTranslations[activeLocale]?.[msg.key] ?? msg.pattern}</span>
						{#if msg.params.length > 0}
							<span class="message-item__params">
								params: {msg.params.join(', ')}
							</span>
						{/if}
					</li>
				{/each}
			</ul>

			<form class="add-form" onsubmit={(e) => { e.preventDefault(); addMessage(); }}>
				<input
					type="text"
					class="add-form__input"
					bind:value={newKey}
					placeholder="Message key"
				/>
				<input
					type="text"
					class="add-form__input"
					bind:value={newPattern}
					placeholder="ICU pattern"
				/>
				<button type="submit" class="btn">Add</button>
			</form>
		</section>

