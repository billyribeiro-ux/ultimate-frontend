<script lang="ts">
	interface MessageDef {
		key: string;
		pattern: string;
		params: string[];
	}

	let messages: MessageDef[] = $state([
		{ key: 'greeting', pattern: 'Welcome, {name}!', params: ['name'] },
		{ key: 'item_count', pattern: '{count, plural, =0 {No items} one {# item} other {# items}}', params: ['count'] },
		{ key: 'page_title', pattern: 'Dashboard', params: [] }
	]);

	let activeLocale: string = $state('en');
	const locales: string[] = ['en', 'pt-BR', 'ar'];

	const localeTranslations: Record<string, Record<string, string>> = {
		'en': { greeting: 'Welcome, {name}!', item_count: '{count, plural, =0 {No items} one {# item} other {# items}}', page_title: 'Dashboard' },
		'pt-BR': { greeting: 'Bem-vindo, {name}!', item_count: '{count, plural, =0 {Nenhum item} one {# item} other {# itens}}', page_title: 'Painel' },
		'ar': { greeting: '!{name} ,مرحبا', item_count: '{count, plural, =0 {لا عناصر} one {عنصر #} two {عنصران} few {# عناصر} many {# عنصرا} other {# عنصر}}', page_title: 'لوحة القيادة' }
	};

	function compileToFunction(key: string, pattern: string): string {
		const params = [...pattern.matchAll(/\{(\w+?)(?:,|\})/g)].map((m) => m[1]);
		const uniqueParams = [...new Set(params)];

		if (uniqueParams.length === 0) {
			return `export function ${key}(): string {\n  return "${pattern}";\n}`;
		}

		const paramTypes: string = uniqueParams
			.map((p) => {
				if (pattern.includes(`${p}, plural`) || pattern.includes(`${p},plural`)) {
					return `${p}: number`;
				}
				return `${p}: string`;
			})
			.join('; ');

		return `export function ${key}(params: { ${paramTypes} }): string {\n  // Compiled from ICU pattern\n  const { ${uniqueParams.join(', ')} } = params;\n  return \`${pattern.replace(/\{(\w+)\}/g, '${$1}')}\`;\n}`;
	}

	let compiledOutput: string = $derived(
		`// Generated by Paraglide — ${activeLocale}.ts\n// Do not edit manually.\n\n` +
		messages
			.map((m) => compileToFunction(m.key, localeTranslations[activeLocale]?.[m.key] ?? m.pattern))
			.join('\n\n')
	);

	let newKey: string = $state('');
	let newPattern: string = $state('');

	function addMessage(): void {
		if (!newKey.trim() || !newPattern.trim()) return;
		const params = [...newPattern.matchAll(/\{(\w+?)(?:,|\})/g)].map((m) => m[1]);
		messages = [...messages, { key: newKey.trim(), pattern: newPattern.trim(), params: [...new Set(params)] }];
		for (const locale of locales) {
			if (!localeTranslations[locale]) localeTranslations[locale] = {};
			localeTranslations[locale][newKey.trim()] = newPattern.trim();
		}
		newKey = '';
		newPattern = '';
	}
</script>

